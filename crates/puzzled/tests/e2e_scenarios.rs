// ===========================================================================
// Scenario 4: Credential Theft Attempt
// ===========================================================================
//
// A compromised agent reads SSH keys from the workspace and attempts to
// commit them to a new location where they could be exfiltrated. The
// policy engine catches the sensitive file pattern (id_rsa, id_ed25519).

#[test]
#[ignore] // Requires root on Linux
fn scenario_credential_theft_attempt() {
    let dir = tempfile::tempdir().unwrap();
    let base_path = dir.path().join("workspace");
    fs::create_dir_all(&base_path).unwrap();

    let manager = make_manager(dir.path());

    let info = manager
        .create("standard", &base_path, 1000, vec![])
        .unwrap();

    // Agent tries to stage SSH keys for exfiltration
    let ssh_dir = info.upper_dir.join(".ssh");
    fs::create_dir_all(&ssh_dir).unwrap();
    fs::write(
        ssh_dir.join("id_rsa"),
        "-----BEGIN OPENSSH PRIVATE KEY-----\n\
         b3BlbnNzaC1rZXktdjEAAAAABG5vbmUAAAA...\n\
         -----END OPENSSH PRIVATE KEY-----\n",
    )
    .unwrap();
    fs::write(
        ssh_dir.join("id_ed25519"),
        "-----BEGIN OPENSSH PRIVATE KEY-----\n\
         b3BlbnNzaC1rZXktdjEAAAAABG5vbmUAAAA...\n\
         -----END OPENSSH PRIVATE KEY-----\n",
    )
    .unwrap();

    // Also tries to stage AWS credentials
    let aws_dir = info.upper_dir.join(".aws");
    fs::create_dir_all(&aws_dir).unwrap();
    fs::write(
        aws_dir.join("credentials"),
        "[default]\n\
         aws_access_key_id = AKIAIOSFODNN7EXAMPLE\n\
         aws_secret_access_key = wJalrXUtnFEMI/K7MDENG/bPxRfiCYEXAMPLEKEY\n",
    )
    .unwrap();

    let result = manager.commit(&info.id).unwrap();
    match result.policy_result {
        PolicyDecision::Rejected(violations) => {
            let messages: Vec<&str> = violations.iter().map(|v| v.message.as_str()).collect();
            let has_ssh = messages
                .iter()
                .any(|m| m.contains("id_rsa") || m.contains(".ssh"));
            let has_aws = messages.iter().any(|m| m.contains(".aws/credentials"));
            assert!(
                has_ssh,
                "should catch SSH key exfiltration, got: {:?}",
                messages
            );
            assert!(
                has_aws,
                "should catch AWS credential exfiltration, got: {:?}",
                messages
            );
            println!(
                "[scenario] Credential theft blocked! {} violation(s):",
                violations.len()
            );
            for v in &violations {
                println!("  - [{}] {}", v.rule, v.message);
            }
        }
        PolicyDecision::Approved => {
            panic!("credential exfiltration attempt should be rejected!");
        }
        PolicyDecision::Error(e) => {
            panic!("unexpected policy error: {e}");
        }
    }

    // Zero residue
    assert!(!base_path.join(".ssh").exists());
    assert!(!base_path.join(".aws").exists());
}

