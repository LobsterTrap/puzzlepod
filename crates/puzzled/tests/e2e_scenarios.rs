/// Build a BranchManager wired to the real policy engine and profiles.
fn make_manager(dir: &std::path::Path) -> puzzled::branch::BranchManager {
    let branch_root = dir.join("branches");
    let manifest_dir = PathBuf::from(env!("CARGO_MANIFEST_DIR"));
    let base_dir = manifest_dir.parent().unwrap().parent().unwrap();
    let profiles_dir = base_dir.join("policies").join("profiles");
    let policies_dir = base_dir.join("policies").join("rules");

    fs::create_dir_all(&branch_root).unwrap();

    let config = puzzled::config::DaemonConfig {
        branch_root: branch_root.clone(),
        profiles_dir: profiles_dir.clone(),
        policies_dir: policies_dir.clone(),
        max_branches: 64,
        bus_type: "session".to_string(),
        fs_type: "ext4".to_string(),
        log_level: "debug".to_string(),
        watchdog_timeout_secs: 30,
        ..Default::default()
    };

    let mut profile_loader = puzzled::profile::ProfileLoader::new(profiles_dir);
    profile_loader.load_all().unwrap();

    let policy_engine = puzzled::policy::PolicyEngine::new(policies_dir);
    policy_engine.reload().unwrap();

    let wal_dir = branch_root.join("wal");
    puzzled::wal::WriteAheadLog::init(&wal_dir).unwrap();
    let wal = puzzled::wal::WriteAheadLog::new(wal_dir);

    let audit = puzzled::audit::AuditLogger::new();
    let conflict_detector = Arc::new(Mutex::new(puzzled::conflict::ConflictDetector::new()));
    let budget_manager = Arc::new(Mutex::new(puzzled::budget::BudgetManager::new()));
    let seccomp_handler = puzzled::seccomp_handler::SeccompNotifHandler::spawn();

    puzzled::branch::BranchManager::new(
        config,
        profile_loader,
        policy_engine,
        wal,
        Arc::new(audit),
        None,
        conflict_detector,
        budget_manager,
        Some(seccomp_handler),
        None,
    )
}

// ===========================================================================
// Scenario 1: Code Assistant — Clean Commit
// ===========================================================================
//
// An AI coding assistant writes new source files in a project workspace.
// The files are normal code — no secrets, no system paths, no persistence
// mechanisms. The commit passes OPA policy and the files appear in the
// base filesystem.

#[test]
#[ignore] // Requires root on Linux
fn scenario_code_assistant_clean_commit() {
    let dir = tempfile::tempdir().unwrap();
    let base_path = dir.path().join("workspace");
    fs::create_dir_all(&base_path).unwrap();

    // Pre-existing project file
    fs::write(base_path.join("README.md"), "# My Project\n").unwrap();

    let manager = make_manager(dir.path());

    // Fork: agent gets an isolated sandbox
    let info = manager
        .create("standard", &base_path, 1000, vec![])
        .unwrap();
    assert_eq!(info.state, BranchState::Active);

    // Explore: agent writes code inside the branch
    let src_dir = info.upper_dir.join("src");
    fs::create_dir_all(&src_dir).unwrap();

    fs::write(
        src_dir.join("main.py"),
        r#"#!/usr/bin/env python3
"""A simple web server generated by the AI coding assistant."""

from http.server import HTTPServer, SimpleHTTPRequestHandler
import json

class APIHandler(SimpleHTTPRequestHandler):
    def do_GET(self):
        if self.path == "/health":
            self.send_response(200)
            self.send_header("Content-Type", "application/json")
            self.end_headers()
            self.wfile.write(json.dumps({"status": "ok"}).encode())
        else:
            super().do_GET()

if __name__ == "__main__":
    server = HTTPServer(("0.0.0.0", 8080), APIHandler)
    print("Server running on port 8080")
    server.serve_forever()
"#,
    )
    .unwrap();

    fs::write(
        src_dir.join("config.py"),
        r#""""Application configuration (no secrets here!)."""

DATABASE_HOST = "localhost"
DATABASE_PORT = 5432
DATABASE_NAME = "myapp"
LOG_LEVEL = "INFO"
"#,
    )
    .unwrap();

    fs::write(
        info.upper_dir.join("requirements.txt"),
        "flask>=3.0\nrequests>=2.31\n",
    )
    .unwrap();

    // Commit: policy approves clean code
    let result = manager.commit(&info.id).unwrap();
    assert!(
        matches!(result.policy_result, PolicyDecision::Approved),
        "clean code commit should be approved, got: {:?}",
        result.policy_result
    );
    assert!(
        result.files_committed >= 3,
        "expected at least 3 files committed, got {}",
        result.files_committed
    );

    // Verify files were committed to base
    assert!(base_path.join("src/main.py").exists());
    assert!(base_path.join("src/config.py").exists());
    assert!(base_path.join("requirements.txt").exists());

    // Verify content integrity
    let main_content = fs::read_to_string(base_path.join("src/main.py")).unwrap();
    assert!(main_content.contains("APIHandler"));

    // Original file still intact
    assert_eq!(
        fs::read_to_string(base_path.join("README.md")).unwrap(),
        "# My Project\n"
    );
}

// ===========================================================================
// Scenario 2: Code Assistant — Secret Leak Prevention
// ===========================================================================
//
// An AI coding assistant is writing a web application. It accidentally
// generates a .env file containing API keys and database credentials.
// The OPA policy engine catches the sensitive file and rejects the commit.
// The base filesystem is untouched — zero residue.

#[test]
#[ignore] // Requires root on Linux
fn scenario_code_assistant_secret_leak() {
    let dir = tempfile::tempdir().unwrap();
    let base_path = dir.path().join("workspace");
    fs::create_dir_all(&base_path).unwrap();

    let manager = make_manager(dir.path());

    let info = manager
        .create("standard", &base_path, 1000, vec![])
        .unwrap();

    // Agent writes legitimate code...
    fs::write(
        info.upper_dir.join("app.py"),
        r#"import os
from flask import Flask

app = Flask(__name__)
db_url = os.environ.get("DATABASE_URL")

@app.route("/")
def index():
    return "Hello, World!"
"#,
    )
    .unwrap();

    // ...but also generates a .env file with secrets (common LLM mistake)
    fs::write(
        info.upper_dir.join(".env"),
        r#"# Auto-generated environment configuration
DATABASE_URL=postgresql://admin:s3cr3t_p@ssw0rd@db.prod.example.com:5432/production
AWS_ACCESS_KEY_ID=AKIAIOSFODNN7EXAMPLE
AWS_SECRET_ACCESS_KEY=wJalrXUtnFEMI/K7MDENG/bPxRfiCYEXAMPLEKEY
STRIPE_SECRET_KEY=sk_test_EXAMPLEKEY567890abcdef
OPENAI_API_KEY=sk-proj-abc123def456
"#,
    )
    .unwrap();

    // Commit: policy REJECTS due to .env file
    let result = manager.commit(&info.id).unwrap();
    match result.policy_result {
        PolicyDecision::Rejected(violations) => {
            assert!(!violations.is_empty());
            assert!(
                violations.iter().any(|v| v.rule == "no_sensitive_files"),
                "should catch .env file, got violations: {:?}",
                violations
            );
            println!(
                "[scenario] Secret leak caught! {} violation(s):",
                violations.len()
            );
            for v in &violations {
                println!("  - [{}] {}", v.rule, v.message);
            }
        }
        PolicyDecision::Approved => {
            panic!("commit with .env file should have been rejected!");
        }
        PolicyDecision::Error(e) => {
            panic!("unexpected policy error: {e}");
        }
    }

    // Zero residue: .env and app.py must NOT appear in base
    assert!(
        !base_path.join(".env").exists(),
        ".env must not leak to base filesystem"
    );
    assert!(
        !base_path.join("app.py").exists(),
        "rejected commit should not apply any files"
    );

    // Branch cleaned up
    assert!(manager.inspect(&info.id).is_none());
}

