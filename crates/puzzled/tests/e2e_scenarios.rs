// ===========================================================================
// Scenario 7: Concurrent Agents — Branch Isolation
// ===========================================================================
//
// Two agents fork from the same base workspace simultaneously. Each writes
// different files. Agent A's commit is approved (clean code). Agent B's
// commit is rejected (writes a .env file). Agent A's changes appear in
// base; Agent B's do not. Proves branches are truly isolated.

#[test]
#[ignore] // Requires root on Linux
fn scenario_concurrent_agent_isolation() {
    let dir = tempfile::tempdir().unwrap();
    let base_path = dir.path().join("workspace");
    fs::create_dir_all(&base_path).unwrap();

    fs::write(base_path.join("shared.txt"), "original shared content\n").unwrap();

    let manager = make_manager(dir.path());

    // Fork two agents from the same base
    let agent_a = manager
        .create("standard", &base_path, 1001, vec![])
        .unwrap();
    let agent_b = manager
        .create("standard", &base_path, 1002, vec![])
        .unwrap();

    // Agent A: writes clean code
    fs::write(
        agent_a.upper_dir.join("feature_a.py"),
        "def feature_a():\n    return 'Agent A was here'\n",
    )
    .unwrap();

    // Agent B: writes code AND a .env file (policy violation)
    fs::write(
        agent_b.upper_dir.join("feature_b.py"),
        "def feature_b():\n    return 'Agent B was here'\n",
    )
    .unwrap();
    fs::write(agent_b.upper_dir.join(".env"), "SECRET=should_not_leak\n").unwrap();

    // Agent A's files should NOT be visible in Agent B's upper layer
    assert!(
        !agent_b.upper_dir.join("feature_a.py").exists(),
        "branches must be isolated: B should not see A's writes"
    );

    // Commit Agent A first — should succeed
    let result_a = manager.commit(&agent_a.id).unwrap();
    assert!(
        matches!(result_a.policy_result, PolicyDecision::Approved),
        "Agent A's clean commit should be approved"
    );

    // Commit Agent B — should be rejected
    let result_b = manager.commit(&agent_b.id).unwrap();
    assert!(
        matches!(result_b.policy_result, PolicyDecision::Rejected(_)),
        "Agent B's .env commit should be rejected"
    );

    // Verify: only Agent A's file appears in base
    assert!(
        base_path.join("feature_a.py").exists(),
        "Agent A's file should be committed"
    );
    assert!(
        !base_path.join("feature_b.py").exists(),
        "Agent B's files should NOT be committed (rejected)"
    );
    assert!(
        !base_path.join(".env").exists(),
        ".env must never reach base"
    );

    // Shared file still intact
    assert_eq!(
        fs::read_to_string(base_path.join("shared.txt")).unwrap(),
        "original shared content\n"
    );
}

